// Generate S-curve funding timeline (slow start, rapid middle, plateau end)
function generateFundingTimeline(finalAmount: number, days: number = 30): FundingDay[] {
  const timeline: FundingDay[] = [];
  for (let day = 1; day <= days; day++) {
    const t = day / days;
    // Sigmoid / S-curve: 1 / (1 + e^(-12*(t-0.4)))
    const s = 1 / (1 + Math.exp(-12 * (t - 0.4)));
    const amount = Math.round(finalAmount * s);
    timeline.push({ day, amount });
  }
  // Ensure last day equals finalAmount
  timeline[timeline.length - 1].amount = finalAmount;
  return timeline;
}

